let comprimento, largura, altura, volume ; 

comprimento = parseFloat(prompt("Por favor informe o comprimento."));
largura = parseFloat(prompt("A largura."));
altura = parseFloat(prompt("A altura."));

volume = comprimento * largura * altura;

alert("O volume da caixa retangular é: " + volume)


